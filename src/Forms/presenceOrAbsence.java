    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
       try{
        int Row = jTable.getSelectedRow();
        txtNumber.setText(jTable.getValueAt(Row, 0).toString());
        txtName.setText(jTable.getValueAt(Row, 1).toString());
        txtJop.setText(jTable.getValueAt(Row, 2).toString());
        txtSuper.setText(jTable.getValueAt(Row, 3).toString());
        txtDay.setText(jTable.getValueAt(Row, 4).toString());
           SimpleDateFormat df =new SimpleDateFormat("yyyy-MM-dd");
           Date h = df.parse(jTable.getValueAt(Row, 5).toString());
           jDateChooser.setDate(h);
           btnAdd.setEnabled(false);
           btnDelete.setEnabled(true);
           btnClear.setEnabled(true);
       }
       catch(Exception ex){
       
         Tools.MsgBox("Erorr");
       }
    }//GEN-LAST:event_jTableMouseClicked

