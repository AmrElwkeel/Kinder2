    private void SetValues(){
       
    a.setIdTeach(Integer.parseInt(txtNumber.getText()));
    a.setNameTeach(txtName.getText());
    a.setJobTeach(txtJop.getText());
    a.setSuberTeach(txtSuper.getText());
    a.setDayTeach(txtDay.getText());
    SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
    String H = String.valueOf(f.format(jDateChooser.getDate()));
    a.setHiring(H);
    
    
    }
