    private void setValues(){
     c.setIdChild(Integer.parseInt(txtId.getText()));
     c.setChildName(txtName.getText());
     c.setAddres(txtAddress.getText());
     c.setPayments(Double.parseDouble(txtPay.getText()));
     SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
     String dateH = String.valueOf(f.format(jDateHiring.getDate()));
     String dateB = String.valueOf(f.format(jDateBirth.getDate()));
        
     c.setHiringDate(dateH);
     c.setBirthDate(dateB);
     c.setKind(ginder);
     String cName = combobox1.getSelectedItem().toString();
     String cNum = cr.getVauleByName(cName);
     c.setClassName(cName);
     c.setClassNum(Integer.parseInt(cNum));
     
       
  
    
    }
